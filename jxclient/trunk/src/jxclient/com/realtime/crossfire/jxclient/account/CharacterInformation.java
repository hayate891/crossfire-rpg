package com.realtime.crossfire.jxclient.account;

/**
 * Information for one character for an account.
 * @author Nicolas Weeger
 */
public class CharacterInformation implements Comparable<CharacterInformation> {
    private String name;
    private String race;
    private int level;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(final String name) {
        this.name = name;
    }

    /**
     * @return the race
     */
    public String getRace() {
        return race;
    }

    /**
     * @param race the race to set
     */
    public void setRace(final String race) {
        this.race = race;
    }

    /**
     * @return the level
     */
    public int getLevel() {
        return level;
    }

    /**
     * @param level the level to set
     */
    public void setLevel(final int level) {
        this.level = level;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int compareTo(final CharacterInformation t) {
        return name.compareTo(t.getName());
    }
}
